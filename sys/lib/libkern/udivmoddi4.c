#include <sys/cdefs.h>

#include "quad.h"

/*
 * Implementation of 64-bit unsigned division/modulo for 32-bit machines.
 */
u_quad_t
__udivmoddi4(u_quad_t n, u_quad_t d, u_quad_t *r)
{
	u_quad_t q = __udivdi3(n, d);
	if (r) {
		*r = n - d * q;
	}
	return (q);
}
